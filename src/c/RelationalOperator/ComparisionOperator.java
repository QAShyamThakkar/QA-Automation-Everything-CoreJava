package c.RelationalOperator;

public class ComparisionOperator {

	public static void main(String[] args) {
		
//		Relational operator alwayse returns boolean value (true, false)
		
//		int a = 10, b = 11;
		int a = 10, b = 10;

		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a < b);

	}

}
