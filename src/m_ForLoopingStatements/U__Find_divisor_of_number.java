package m_ForLoopingStatements;

// Eg for 6, it is 1,2,3,6 (with 4 and 5 we can not divide 6)

public class U__Find_divisor_of_number {

public static void main(String[] args) {
	
	int num=10;
	
	
	for (int i = 1; i <=num; i++) {

		if (num%i==0) {
			System.out.println(i);
		}
		
	}
	
	
	
	
}

	
	
	
}
