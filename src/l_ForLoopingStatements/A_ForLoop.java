package l_ForLoopingStatements;

public class A_ForLoop {

//	Purpose - we're controlling while loop with initialization, condition & increment / decrement
//
//	If we miss any one of them then our loop will print different output or it may go into infininte execution 
//	So FOR loop bring all loop contorl under one brackes so inside the loop we can just focucs on repetation
//
//	Syntax
//
//		for(initialization : Condition 	: Increment/Decrement)
//		{
//			// block of statements will be executed till condition is true
//		}
//
//	Note - Execution flow 
//		First Time - 1.initialization 2. condition 3. block of code 4. increment decrement
//		From 2nd Time - 2. condition 3. block of code 4. increment decrement	
	
}
